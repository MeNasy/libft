#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	size_t	sayac;
	char	*cs;

	i = 0;
	sayac = 0;
	while (i <= ft_strlen(s))
	{
		if (s[i] == (char)c)
		{
			cs = (char *) s + i;
			sayac = 1;
		}
		i++;
	}
	if (sayac > 0)
		return (cs);
	return (0);
}
